x="How are you?"
# uppercase
print(x.upper())
#lowercase
print(x.lower())
# Remove White Space at both side )
print(x.strip())

# Remove White Space at left side)
print(x.lstrip())

# Remove White Space at right side)
print(x.rstrip())

# Replacement 

print(x.replace("How","Where"))


# Split

print(x.split(" "))